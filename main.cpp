#include <iostream>
using namespace std;

void sumar() {
    int a, b;
    cout << "Ingrese dos numeros: ";
    cin >> a >> b;
    cout << "Suma: " << (a + b) << endl;
}

void restar() {
    int a, b;
    cout << "Ingrese dos numeros: ";
    cin >> a >> b;
    cout << "Resta: " << (a - b) << endl;
}